DROP TABLE IF EXISTS pref;
CREATE TABLE pref (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY, 
    account INTEGER REFERENCES account(id) NOT NULL,
    gong boolean DEFAULT true,
    _1 INTEGER DEFAULT 0,
    _2 INTEGER DEFAULT 0,
    _3 INTEGER DEFAULT 0,
    _5 INTEGER DEFAULT 0,
    _8 INTEGER DEFAULT 0,
    _13 INTEGER DEFAULT 0,
    _21 INTEGER DEFAULT 0,
    _34 INTEGER DEFAULT 0,
    _55 INTEGER DEFAULT 0,
    _89 INTEGER DEFAULT 0,
    date_published TIMESTAMPTZ DEFAULT now() NOT NULL
);